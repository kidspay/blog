function rechercher() {

    var a = document.getElementById("search").value;

    if (a === "bitcoin") {
        window.open("bitcoin.html#2");
    }

    if (a === "Bitcoin") {
        window.open("bitcoin.html#2");
    }

    if (a === "ethereum") {
        window.open("ethereum.html#1");
    }

    if (a === "Ethereum") {
        window.open("ethereum.html#1");
    }

    if (a === "blockchain") {
        window.open("ethereum.html#0");
    }

    if (a === "Blockchain") {
        window.open("ethereum.html#0");
    }
}
